import Foundation

struct userLocation {
    var user: String = ""
    var userName: String = ""
    var location: String = ""

    func findUser() -> String
    {
        return user
    }
    func findLocation() -> String
    {
        return location
    }
    func displayUserName() -> String
    {
        return userName
    }
}
